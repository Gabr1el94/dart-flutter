void main() {
    /* Operators Math */
    double num1 = 10.0;
    double num2 = 20.0;

    print(num1+num2);
    print(num1-num2);
    print(num1*num2);
    print(num2/num1);

    /* Operators Logic */
    bool testComp = (10 > 20); // == != > <
    print(testComp);

    // Operator OR
    bool testOr = (true || false);
    print(testOr);

    // Operator AND
    bool testAnd = (false && true);
    print(testAnd); 
}